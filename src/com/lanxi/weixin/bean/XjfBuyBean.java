package com.lanxi.weixin.bean;

public class XjfBuyBean extends XjfHeadBean {

	private String phone = "";
	private String type = "";
	private String skuCode = "";
	private String count = "";
	private String needSend = "";
	private String remark = "";
	
	private String totalAmt = "";
	private String skuList = "";
	private String amt = "";
	private String code = "";
	private String endTime = "";
	
	public String getBodyXml(){
		StringBuffer sb = new StringBuffer();
		sb.append("<MSG>")
		.append("<Phone>").append(this.phone).append("</Phone>")
		.append("<Type>").append(this.type).append("</Type>")
		.append("<SkuCode>").append(this.skuCode).append("</SkuCode>")
		.append("<Count>").append(this.count).append("</Count>")
		.append("<NeedSend>").append(this.needSend).append("</NeedSend>")
		.append("<Remark>").append(this.remark).append("</Remark>")
		.append("</MSG>");
		return sb.toString();
	}
	
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getSkuCode() {
		return skuCode;
	}
	public void setSkuCode(String skuCode) {
		this.skuCode = skuCode;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getNeedSend() {
		return needSend;
	}
	public void setNeedSend(String needSend) {
		this.needSend = needSend;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getTotalAmt() {
		return totalAmt;
	}
	public void setTotalAmt(String totalAmt) {
		this.totalAmt = totalAmt;
	}
	public String getSkuList() {
		return skuList;
	}
	public void setSkuList(String skuList) {
		this.skuList = skuList;
	}
	public String getAmt() {
		return amt;
	}
	public void setAmt(String amt) {
		this.amt = amt;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	
}
