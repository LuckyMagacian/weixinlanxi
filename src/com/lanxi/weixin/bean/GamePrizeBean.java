package com.lanxi.weixin.bean;

public class GamePrizeBean {

	/**游戏类型*/
	private String gameType;
	/**奖品等级*/
	private String prizeGrade;
	/**商品编号*/
	private String spbh;
	/**商品数量*/
	private String spsl;
	/**创建日期*/
	private String createTime;
	/**商品状态	1-上架，2-下架*/
	private String spzt;
	/**游戏积分*/
	private String points;
	/**备用*/
	private String beiy;
	public String getGameType() {
		return gameType;
	}
	public void setGameType(String gameType) {
		this.gameType = gameType;
	}
	public String getPrizeGrade() {
		return prizeGrade;
	}
	public void setPrizeGrade(String prizeGrade) {
		this.prizeGrade = prizeGrade;
	}
	public String getSpbh() {
		return spbh;
	}
	public void setSpbh(String spbh) {
		this.spbh = spbh;
	}
	public String getSpsl() {
		return spsl;
	}
	public void setSpsl(String spsl) {
		this.spsl = spsl;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getSpzt() {
		return spzt;
	}
	public void setSpzt(String spzt) {
		this.spzt = spzt;
	}
	public String getPoints() {
		return points;
	}
	public void setPoints(String points) {
		this.points = points;
	}
	public String getBeiy() {
		return beiy;
	}
	public void setBeiy(String beiy) {
		this.beiy = beiy;
	}
	
}
