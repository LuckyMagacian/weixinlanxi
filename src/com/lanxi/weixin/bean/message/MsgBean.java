package com.lanxi.weixin.bean.message;

public class MsgBean {
	
	/**开发者微信号*/
	protected String toUserName;
	/**发送方帐号（一个OpenID）*/
	protected String fromUserName;
	/**消息创建时间 （整型）*/
	protected long createTime;
	/**消息类型*/
	protected String msgType;
	
	/**请求：文本类型*/
	public final static String REQ_MSG_TYPE_TEXT = "text"; 
	/**请求：图片类型*/
	public final static String REQ_MSG_TYPE_IMAGE = "image";
	/**请求：语音类型*/
	public final static String REQ_MSG_TYPE_VOICE = "voice";
	/**请求：视频类型*/
	public final static String REQ_MSG_TYPE_VIDEO = "video";
	/**请求：小视频类型*/
	public final static String REQ_MSG_TYPE_SHORTVIDEO = "shortvideo";
	/**请求：地理位置类型*/
	public final static String REQ_MSG_TYPE_LOCATION = "location";
	/**请求：链接类型*/
	public final static String REQ_MSG_TYPE_LINK = "link";
	/**请求：事件类型*/
	public final static String REQ_MSG_TYPE_EVENT = "event";
	
	/**回复：文本类型*/
	public final static String RESP_MSG_TYPE_TEXT = "text";
	/**回复：图片类型*/
	public final static String RESP_MSG_TYPE_IMAGE = "image";
	/**回复：语音类型*/
	public final static String RESP_MSG_TYPE_VOICE = "voice";
	/**回复：视频类型*/
	public final static String RESP_MSG_TYPE_VIDEO = "video";
	/**回复：音乐类型*/
	public final static String RESP_MSG_TYPE_MUSIC = "music";
	/**回复：图文类型*/
	public final static String RESP_MSG_TYPE_NEWS = "news";
	
	public String getToUserName() {
		return toUserName;
	}
	public void setToUserName(String toUserName) {
		this.toUserName = toUserName;
	}
	public String getFromUserName() {
		return fromUserName;
	}
	public void setFromUserName(String fromUserName) {
		this.fromUserName = fromUserName;
	}
	public String getMsgType() {
		return msgType;
	}
	public void setMsgType(String msgType) {
		this.msgType = msgType;
	}
	public long getCreateTime() {
		return createTime;
	}
	public void setCreateTime(long createTime) {
		this.createTime = createTime;
	}
	
}
